package Aula_05;

public class App {
    public static void main(String[] args) {
        int x = 24;

        if (x > 00 && x <= 12) {
            System.out.println("Bom dia");
        } else if (x > 12 && x <= 17) {
            System.out.println("Boa Tarde");
        } else if (x > 17 && x <= 24) {
            System.out.println("Boa Noite");
        }
    }
}
