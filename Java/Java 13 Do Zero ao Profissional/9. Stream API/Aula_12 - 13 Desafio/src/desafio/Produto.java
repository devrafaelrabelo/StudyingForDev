package desafio;

public class Produto {
	
	String nome;
	double valor;
	double desconto;
	double frete;
	
	
	public Produto(String nome, double valor, double desconto, double frete) {
		this.nome = nome;
		this.valor = valor;
		this.desconto = desconto;
		this.frete = frete;
	}	
	
	
}
