package resposta;

public class Produto {
	
	final String nome;
	final double preco;
	final double desconto;
	final double frete;
	
	
	public Produto(String nome, double valor, double desconto, double frete) {
		this.nome = nome;
		this.preco = valor;
		this.desconto = desconto;
		this.frete = frete;
	}	
	
	
}
